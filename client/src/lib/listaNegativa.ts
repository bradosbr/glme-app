/**
 * Verifica se uma NCM está na lista negativa do Edital 060/2025
 * Retorna true se a NCM está impedida de usar o PEAP (diferimento)
 */
export function ncmNaListaNegativa(ncm: string): boolean {
  if (!ncm) return false;
  // Normalizar: remover pontos e espaços, manter apenas dígitos
  const ncmLimpo = ncm.replace(/[^0-9]/g, "").trim();
  if (ncmLimpo.length < 4) return false;
  
  // Verificar correspondência exata (8 dígitos)
  if (ncmLimpo.length === 8) {
    return LISTA_NEGATIVA_NCM.includes(ncmLimpo);
  }
  
  // Verificar prefixo (ex: NCM "8711" cobre "87112090")
  return LISTA_NEGATIVA_NCM.some(item => item.startsWith(ncmLimpo) || ncmLimpo.startsWith(item.substring(0, ncmLimpo.length)));
}
